def GetDestination(xcoord, idir, lngth):
    idim = idir // 2
    newcoord = list(xcoord)
    oldcoorddim = newcoord[idim]
    if idir % 2 == 0:
        # go "up"
        newcoord[idim] = (oldcoorddim + 1) % lngth
