def BoseOutM(inputarcarr):
    """ 
    This is the inner core that gets called by Pedantic or some similar outer shell routine; 
    note that the Z-token configuration is not among the inputs. (The parameter M is
    set via the option flags)

    Unlike the FermiOut6 routine, this relies of FilledIn (which was cded later so as to 
    more systematically "even out" a function, whereas FermiOut6 was just some inital passes
    at two ways of doing that (see the subroutines on FermiOut6). That means that  when M=1, the
    results will differ from run to run, but statistically speaking, BoseOutM with M=1 is
    the same routine as FermiOut6. The latter has been retained since it was coded first, but
    in light of BoseOutM(with M=1) it is obsolete.
    """

    global PARTICLE_LIMIT

    if PARTICLE_LIMIT == 0:
        return BoseOutVaryM(inputarcarr)
    M = PARTICLE_LIMIT

    N = len(inputarcarr)
    modpoolout = copy(inputarcarr) # we can call it modpoolout since inputarcarr is the result of a ModPool calculation (see the calling function)

    if np.max(np.abs(modpoolout)) <= M:
        return modpoolout,  np.zeros((N, )).astype("int")

    retarr = copy(modpoolout)
    if max(retarr) > M:
        retarr = FillIn(retarr - M)
        retarr = retarr + M
    if min(retarr) < -M:
        retarr = FillIn(-retarr - M)
        retarr = -retarr - M

    return retarr, modpoolout - retarr



